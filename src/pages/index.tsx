import { useState } from "react";
import styled from "styled-components";
import Head from "next/head";
import type { PlayerData } from "../types/PlayerData";
import type { Avatar } from "@/types/Avatar";
import type { Color } from "@/types/Color";
import NewGameForm from "@/components/NewGameForm";
import Player from "@/components/Player";

interface HomeProps {
  players: PlayerData[];
  avatars: Avatar[];
  colors: Color[];
}

const Main = styled.main`
  display: grid;
  grid-template-areas: "headline headline headline" "title title title" "user-input form form";
`;

const Headline = styled.h1`
  grid-area: headline;
`;

const Title = styled.h2`
  grid-area: title;
`;

const CurrentStateOfUserInput = styled.div`
  grid-area: user-input;
  display: flex;
  flex-direction: column;
  align-items: center;
  justify-content: center;
  gap: 0.5rem;
  border: 0.4rem solid black;
  border-right: 0.2rem solid black;
  border-radius: 0.5rem 0 0 0.5rem;
  padding: 1rem;

  & p {
    margin: 0;
  }
`;

export default function Home({ avatars, colors }: HomeProps) {
  const [userName, setUserName] = useState("Silent Parrot");
  const [selectedAvatar, setSelectedAvatar] = useState("charlie");
  const [selectedColor, setSelectedColor] = useState("");
  const [currentStep, setCurrentStep] = useState(1);

  function updateSelectedAvatar(name: string) {
    setSelectedAvatar(name);
  }

  function updateSelectedColor(colorCode: string) {
    setSelectedColor(colorCode);
  }

  function updateCurrentStep(step: number) {
    setCurrentStep(step);
  }

  function updateUserName(name: string) {
    setUserName(name);
  }

  return (
    <>
      <Head>
        <title>Monopoly (on Fire)</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Main>
        <Headline>Monopoly (on Fire)</Headline>
        <Title id="game-start">Start a new game:</Title>
        <CurrentStateOfUserInput>
          <Player
            selectedAvatar={selectedAvatar}
            selectedColor={selectedColor}
            userName={userName}
            colors={colors}
          />
        </CurrentStateOfUserInput>
        <NewGameForm
          userName={userName}
          avatars={avatars}
          colors={colors}
          currentStep={currentStep}
          selectedAvatar={selectedAvatar}
          updateSelectedAvatar={updateSelectedAvatar}
          selectedColor={selectedColor}
          updateSelectedColor={updateSelectedColor}
          updateCurrentStep={updateCurrentStep}
          updateUserName={updateUserName}
        />
      </Main>
    </>
  );
}
